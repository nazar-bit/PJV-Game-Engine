package cz.cvut.fel.pjv.weapons;

/**
 * BulletType enumerates the various types of ammunition available in the game.
 *
 * <p>This enum is used to distinguish between different bullet types for weapons, such as
 * pistol, SMG, rifle, and shotgun. The NONE value represents the absence of a valid ammo type.</p>
 */
public enum BulletType {
    PISTOL_AMMO,
    SMG_AMMO,
    RIFLE_AMMO,
    SHOTGUN_AMMO,
    NONE,
}
