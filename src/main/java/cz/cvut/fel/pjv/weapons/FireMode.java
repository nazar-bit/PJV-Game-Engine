package cz.cvut.fel.pjv.weapons;

/**
 * FireMode enumerates the different firing modes available for weapons in the game.
 *
 * <p>
 * Each constant represents a specific mode of operation, such as single shot (MANUAL),
 * continuous firing (AUTOMATIC), burst fire (BURST), or melee combat (MELEE).
 * </p>
 */
public enum FireMode {
    MANUAL,
    AUTOMATIC,
    BURST,
    MELEE,
}
